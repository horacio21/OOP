    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String email = txtEmail.getText();
        if(email.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Debe Ingresar Un Email");
            txtEmail.requestFocus();
            return;
        }
        
        String password = new String ((txtPassword.getPassword()));
        if(password.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Debe Ingresar Una Contraseña");
            txtPassword.requestFocus();
            return;
        }
        
        User user = UserDAO.login(email, password);
        
        if( user == null ){
            JOptionPane.showMessageDialog(rootPane, "Email y Contraseña No Coinciden.");
            txtPassword.setText("");
            txtPassword.requestFocus();
        }
        
        JOptionPane.showMessageDialog(rootPane, "Bienvenido " + user.getFirstName()
                + " " + user.getLastName());
        
        FrmMain form = new FrmMain();
        form.setUser(user);
        this.setVisible(false);
        form.setLocationRelativeTo(null);
        form.setVisible(rootPaneCheckingEnabled);
    }//GEN-LAST:event_btnLoginActionPerformed

