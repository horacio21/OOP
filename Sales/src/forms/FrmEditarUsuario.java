    private void btnCrearUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearUsuarioActionPerformed
        // TODO add your handling code here:
        if(txtEmail.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Debe Ingresar Un Email");
            txtEmail.requestFocus();
            return;
        }
        if(txtLastName.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Debe Ingresar Al Menos Un Apellido");
            txtLastName.requestFocus();
            return;
        }
        if(txtFirstName.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Debe Ingresar Al Menos Un Nombre");
            txtFirstName.requestFocus();
            return;
        }
        
        String password = new String(txtPassword.getPassword());
        if(password.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Debe Ingresar Una Contraseña");
            txtPassword.requestFocus();
            return;
        }
        
        String confirmarPassword = new String(txtConfirmarPassword.getPassword());
        if(!password.equals(confirmarPassword)){
            JOptionPane.showMessageDialog(rootPane, "Las Contraseñas No Coinciden");
            txtConfirmarPassword.requestFocus();
            return;
        }
        
        User user = new User(txtEmail.getText(), 
                txtLastName.getText(), 
                txtFirstName.getText(), 
                password, 
                cmbRol.getSelectedIndex());
        
        this.user = user;
        this.dispose();
    }//GEN-LAST:event_btnCrearUsuarioActionPerformed

